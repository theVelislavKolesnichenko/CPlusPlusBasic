#include <iostream>

using namespace std;

main()
{
  float a, b, s, p;

  cout << "Въведете страната на правоъгълника: ";
  cin >> a;

  cout << "Въведете втората страна на правоъгълника: ";
  cin >> b;

  s = a * b;

  p = 2 * (a + b);

  cout << "Стойнтостта на лицето на правоъгълника е равна на: " << s << endl;
  cout << "Стойнтостта на обиколката на правоъгълника е равна на: " << p;
}
